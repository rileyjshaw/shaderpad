import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import prettier from 'prettier';

import { errors, ERROR_DOCS_BASE_URL } from './error-codes.mjs';

const scriptsDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptsDir, '..');
const runtimeOutputPath = join(repoRoot, 'packages', 'shaderpad', 'src', 'internal', 'error-codes.gen.ts');
const runtimeDevOutputPath = join(repoRoot, 'packages', 'shaderpad', 'src', 'internal', 'error-codes.dev.gen.ts');
const docsOutputPath = join(repoRoot, 'docs', 'src', 'generated', 'error-codes.ts');

/**
 * @param {ReadonlyArray<{ code: number }>} entries
 */
function assertUniqueErrorCodes(entries) {
	const uniqueCodes = new Set(entries.map(entry => entry.code));
	if (entries.length !== uniqueCodes.size) {
		throw new Error('Error registry contains duplicate codes.');
	}

	return entries;
}

const uniqueErrors = assertUniqueErrorCodes(errors);

async function formatGeneratedArtifact(source, filepath) {
	const options = (await prettier.resolveConfig(filepath)) ?? {};
	return prettier.format(source, { ...options, filepath });
}

function renderRuntimeArtifact(entries) {
	return `// Generated by scripts/generate-error-code-artifacts.mjs. Do not edit manually.
export type ErrorCode = number;

export const ERROR_DOCS_BASE_URL = ${JSON.stringify(ERROR_DOCS_BASE_URL)};

export function errorUrl(code: ErrorCode) {
\treturn \`\${ERROR_DOCS_BASE_URL}\${code}\`;
}
`;
}

function renderDocsArtifact(entries) {
	return `// Generated by scripts/generate-error-code-artifacts.mjs. Do not edit manually.
export type ErrorEntry = {
\tcode: number;
\ttitle: string;
\tsummary: string;
\tcauses: readonly string[];
\tfixes: readonly string[];
};

export const ERROR_DOCS_BASE_URL = ${JSON.stringify(ERROR_DOCS_BASE_URL)};

export function errorUrl(code: number) {
\treturn \`\${ERROR_DOCS_BASE_URL}\${code}\`;
}

export const errors: readonly ErrorEntry[] = ${JSON.stringify(entries, null, '\t')};

export function getErrorByCode(code: string | number) {
\tconst normalizedCode = String(code);
\treturn errors.find(error => String(error.code) === normalizedCode);
}
`;
}

function renderRuntimeDevArtifact(entries) {
	const devErrors = Object.fromEntries(
		entries.map(entry => [
			entry.code,
			{
				title: entry.title,
				summary: entry.summary,
			},
		]),
	);
	return `// Generated by scripts/generate-error-code-artifacts.mjs. Do not edit manually.
import type { ErrorCode } from './error-codes.gen.js';

type DevErrorDetails = {
\ttitle: string;
\tsummary: string;
};

export const DEV_ERRORS: Record<ErrorCode, DevErrorDetails> | undefined = __SHADERPAD_DEV__
\t? ${JSON.stringify(devErrors, null, '\t')}
\t: undefined;
`;
}

await mkdir(dirname(runtimeOutputPath), { recursive: true });
await mkdir(dirname(runtimeDevOutputPath), { recursive: true });
await mkdir(dirname(docsOutputPath), { recursive: true });
await writeFile(
	runtimeOutputPath,
	await formatGeneratedArtifact(renderRuntimeArtifact(uniqueErrors), runtimeOutputPath),
	'utf8',
);
await writeFile(
	runtimeDevOutputPath,
	await formatGeneratedArtifact(renderRuntimeDevArtifact(uniqueErrors), runtimeDevOutputPath),
	'utf8',
);
await writeFile(
	docsOutputPath,
	await formatGeneratedArtifact(renderDocsArtifact(uniqueErrors), docsOutputPath),
	'utf8',
);

console.log(`Generated ${runtimeOutputPath}`);
console.log(`Generated ${runtimeDevOutputPath}`);
console.log(`Generated ${docsOutputPath}`);
